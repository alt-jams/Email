    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        if (tbMensagem.getSelectedRowCount() != 0){
            int id = tbModel.getMensagem(tbMensagem.getSelectedRow()).getId();
            Mensagem m = funcoes.encontraMensagem(id);
                       
                if(op == 0){//caixa de entrada
                    m.setViewDestinatario(0);
                    funcoes.apaga(m);
                    tbModel.carregarDadosEntrada(user);
                    
                }if(op == 1){//caixa de saída
                    m.setViewRemetente(0);
                    funcoes.apaga(m);
                    tbModel.carregarDadosSaida(user);  
                    
                }if(op == 2){
                    m.setViewDestinatario(1);
                    funcoes.apaga(m);
                    tbModel.carregarLixeira(user); 
                }
                
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma mensagem");
        }
    }//GEN-LAST:event_excluirActionPerformed

