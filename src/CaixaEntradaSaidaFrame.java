    private void abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirActionPerformed
        if (tbMensagem.getSelectedRowCount() != 0){
            setVisible(false);
            int id = tbModel.getMensagem(tbMensagem.getSelectedRow()).getId();
                if(op == 0){
                    VisualizarMensagemFrame visualiza = new VisualizarMensagemFrame(id, user,0);
                    visualiza.setVisible(true);
                }else{
                    VisualizarMensagemFrame visualiza = new VisualizarMensagemFrame(id, user,1);
                    visualiza.setVisible(true);
                }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma mensagem");
        }
    }//GEN-LAST:event_abrirActionPerformed

